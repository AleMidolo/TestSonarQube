import re

def is_ipv4(target):
    """
    IPv4 पते की जांच करें या नहीं।
    """
    pattern = r'^(25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\.' \
              r'(25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\.' \
              r'(25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\.' \
              r'(25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)$'
    return re.match(pattern, target) is not None